package nig.parietal;

/**
 *
 * @author nigel
 */
public class Reaction {
    private String emotion;
    private String response;
    private String deficiency;
    private int id;
    private String escResponse;
    private String escDeficiency;
    private int emotion_id;
    private String type;
    /**
     * @return the emotion
     */
    public String getEmotion() {
        return emotion;
    }

    /**
     * @param emotion the emotion to set
     */
    public void setEmotion(String emotion) {
        this.emotion = emotion;
    }

    /**
     * @return the response
     */
    public String getResponse() {
        return response;
    }

    /**
     * @param response the response to set
     */
    public void setResponse(String response) {
        this.response = response;
    }

    /**
     * @return the deficiency
     */
    public String getDeficiency() {
        return deficiency;
    }

    /**
     * @param deficiency the deficiency to set
     */
    public void setDeficiency(String deficiency) {
        this.deficiency = deficiency;
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the escResponse
     */
    public String getEscResponse() {
        return escResponse;
    }

    /**
     * @param escResponse the escResponse to set
     */
    public void setEscResponse(String escResponse) {
        this.escResponse = escResponse;
    }

    /**
     * @return the escDeficiency
     */
    public String getEscDeficiency() {
        return escDeficiency;
    }

    /**
     * @param escDeficiency the escDeficiency to set
     */
    public void setEscDeficiency(String escDeficiency) {
        this.escDeficiency = escDeficiency;
    }

    /**
     * @return the emotion_id
     */
    public int getEmotion_id() {
        return emotion_id;
    }

    /**
     * @param emotion_id the emotion_id to set
     */
    public void setEmotion_id(int emotion_id) {
        this.emotion_id = emotion_id;
    }

    /**
     * @return the type
     */
    public String getType() {
        return type;
    }

    /**
     * @param type the type to set
     */
    public void setType(String type) {
        this.type = type;
    }
}
